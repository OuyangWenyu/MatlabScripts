function b = findAdjHUC( HUCid )
% find out if given HUCs are adjacent.

% input
% HUCid - an array of HUC ids (1 - 18, see H:\Kuai\map\HUC\HUC2_CONUS.shp, field HUC02)

% output
% b - 1 for adjacent, 0 for not adjacent

% example
%{
HUCid=[1,2,3,4];
b=findAdjHUC(HUCid);
%}

%% Adjacent table
tab={...
    [2,4];...
    [1,3,4,5];...
    [2,5,6,8];...
    [1,2,5,7,9];...
    [2,3,4,6,7,8];...
    [3,5,8];...
    [4,5,8,9,10,11];...
    [3,5,6,7,11,12];...
    [4,7,10];...
    [7,9,11,14,17];...
    [7,8,10,12,13,14];...
    [8,11,13];...
    [11,12,14,15];...
    [10,11,13,15,16,17];...
    [13,14,16,18];...
    [14,15,17,18];...
    [10,14,16,18];...
    [15,16,17];...
    };

%% find if adjacent
b=1;
if length(HUCid)>1
    for k=1:length(HUCid)
        cID=HUCid(k);
        rID=HUCid;rID(k)=[];
        adjID=tab{cID};
        [C,ind1,ind2]=intersect(rID,adjID);
        b=b&~isempty(C);
    end
end



end

